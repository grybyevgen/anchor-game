-- Миграция: обновление начального баланса пользователей до 6000 монет
-- Обновляет баланс только для пользователей с балансом меньше 6000

UPDATE users 
SET coins = 6000
WHERE coins < 6000;

-- Комментарий для разработчиков
COMMENT ON COLUMN users.coins IS 'Баланс пользователя. Начальный баланс: 6000 монет (покупка судна + загрузка груза + заправка + запас на ошибки)';
